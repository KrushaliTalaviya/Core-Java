package program;

/**
 * Practical no - 4 : Write a program to demonstrate the use of do-while loop
 * print 1 to 10
 *
 * @author Krushali
 */
public class Dowhileloop {

	public static void main(String[] args) {
		// Declare variable
		int number = 1;

		// Do-while loop 1 to 10
		do {
			System.out.println(number);
			number++;
		} while (number <= 10);
	}

}
