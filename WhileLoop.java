package program;

/**
 * Practical no-4 : Write a program to demonstrate the use of while loop print 1
 * to 10
 *
 * @author Krushali
 */
public class WhileLoop {

	public static void main(String[] args) {
		// Declare variable
		int number = 1;

		// While loop from 1 to 10
		while (number <= 10) { //
			System.out.println(number);
			number = number + 1;
		}
	}
	
}
